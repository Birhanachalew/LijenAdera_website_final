export const normalizeLocale = (value) => {
  if (!value) return DEFAULT_LOCALE;
  const normalized = value.toLowerCase().split("-")[0];
  return SUPPORTED_LOCALES.includes(normalized) ? normalized : DEFAULT_LOCALE;
};

const getValueByPath = (object, key) => {
  if (!key) return undefined;
  return key.split(".").reduce((acc, part) => (acc ? acc[part] : undefined), object);
};

export const createTranslator = (locale) => {
  const resolvedLocale = normalizeLocale(locale);
  const dictionary = dictionaries[resolvedLocale] || dictionaries[DEFAULT_LOCALE];
  const fallbackDictionary = dictionaries[DEFAULT_LOCALE];

  return (key, fallback) => {
    const value = getValueByPath(dictionary, key);
    if (value !== undefined) return value;

    const fallbackValue = getValueByPath(fallbackDictionary, key);
    if (fallbackValue !== undefined) return fallbackValue;

    return fallback !== undefined ? fallback : key;
  };
};
